// User function template for C++
class Solution {
  public:
    void pushZerosToEnd(vector<int>& arr) {
        // code here
        vector<int>ans(arr.size());
        int c=0;
        for(int i=0;i<arr.size();i++)
        {
            if(arr[i]!=0)
            {
                ans[c]=arr[i];
                c++;
            }
        }
        for(int i=c;i<arr.size();i++)
        {
            ans[i]=0;
            i++;
        }
        arr=ans;
    }
};

