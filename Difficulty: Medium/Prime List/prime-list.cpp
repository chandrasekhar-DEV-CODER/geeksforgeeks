class Solution {
  public:
bool isPrime(int n) {
    if (n <= 1) return false;
    if (n <= 3) return true;
    if (n % 2 == 0 || n % 3 == 0) return false;
    for (int i = 5; i * i <= n; i += 6) {
        if (n % i == 0 || n % (i + 2) == 0) return false;
    }
    return true;
}

// Function to find the nearest prime to a given number
int findNearestPrime(int num) {
    if (isPrime(num)) return num; // If the number itself is prime

    int lower = num - 1;
    int upper = num + 1;

    // Move outwards from num to find the nearest prime
    while (true) {
        if (lower > 1 && isPrime(lower)) return lower;  // Check lower prime
        if (isPrime(upper)) return upper;               // Check upper prime
        lower--;
        upper++;
    }
}

// Function to modify the linked list with nearest primes
Node* primeList(Node* head) {
    Node* temp = head;

    // Traverse the list and replace each value with its nearest prime
    while (temp != NULL) {
        temp->data = findNearestPrime(temp->data);
        temp = temp->next;
    }

    return head;
}


};

